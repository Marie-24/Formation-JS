//  Affichage du résultat de la console
console.log("la multiplication de nb1*nb2 est égale à:"+resultat);

//--division de nb1 et nb2 avec l'opérateur "/"
resultat=nb1/nb2;
//  Affichage du résultat de la console
console.log("la division de nb1/nb2 est égale à:"+resultat);

//--le modulo de nb1 et nb2 avec l'opérateur "%"
nb1=11;
resultat=nb1 % nb2;
//  Affichage du résultat de la console
